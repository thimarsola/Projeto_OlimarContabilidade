         <div class="product-07__content mb-8 t-justify">
             <p>A Contabilidade é uma ciência que tem por objetivo o estudo das variações quantitativas e qualitativas ocorridas no patrimônio (conjunto de bens, direitos e obrigações) das entidades (pessoa física ou jurídica que possui um patrimônio), podendo assim fornecer o máximo de informações úteis para as tomadas de decisões, registrando e controlando o patrimônio.</p>
             <br>
             <p>Em resumo, a Contabilidade abrange um conjunto de técnicas para controlar o patrimônio das organizações mediante a aplicação do seu grupo de princípio, técnicas, normas e procedimentos próprios.</p>
             <br>
             <p>Todas as movimentações existentes no patrimônio de uma entidade são registradas pela Contabilidade, que resume os fatos em forma de relatórios e entrega-os aos interessados em saber como está indo a situação da empresa.</p>
             <br>
             <p>Partindo destes relatório são analisados os resultados alcançados e a partir daí são tomadas decisões em relação aos acontecimentos futuros. Sendo assim a Contabilidade é responsável pela escrituração (registro em livros próprios) e apuração destes resultados e é só através dela que há condições para se apurar o lucro ou prejuízo em determinado período.</p>
