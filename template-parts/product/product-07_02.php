             <p>Através das atividades realizadas nesse setor que a empresa pode conseguir benefícios fiscais, reduzir custos e buscar créditos de impostos para compensação.</p>
             <br>
             <p>Dentre as várias atividades do setor fiscal, destaca-se a apuração de impostos como uma das mais importantes, já que é preciso realizar o pagamento em dia, evitando encargos e aumentando os custos da empresa.</p>
             <br>
             <p>Outras responsabilidades são a preparação, conferência e entrega de Obrigações Acessórias: GIA, DeSTDA, GISS, Sintegra, Nota Fiscal Paulista, Nota Fiscal Paulistana, D-Sup, Cpom, Dipam, DCTF, Dmed, Dmob, Sped Contribuições, Sped Fiscal, Sinal, Auxílio na Emissão de Notas Fiscais, Acompanhamento da Situação Fiscal e Tributária, Classificação e Lançamento de Documentos Fiscais, Atendimentos à Fiscalização.</p>
             <br>
             <p>O setor fiscal não tem apenas o objetivo de escriturar notas fiscais e apurar impostos, é através das informações geradas pela área fiscal e contábil que as empresas identificam oportunidades e melhoram a saúde do seu negócio.</p>
             <br>
            <p>Atendemos empresas do Lucro Presumido, Simples Nacional, MEI e Autônomos.</p>
             <br>
             <p>No Lucro Presumido, a tributação dos impostos é feita de forma mais simplificada, aplicando percentuais fixos sobre determinadas receitas. Entretanto, no Lucro Presumido há um limite para poder optar por este regime, que é a receita bruta do ano anterior. A partir de 2014, o limite é de R$ 78 milhões.</p>
             <br>
             <p>Já o Simples Nacional é uma forma simplificada de cálculo e recolhimento dos tributos, aplicável às microempresas e empresas de pequeno porte. Porém, nem todas podem optar por essa forma de tributação. A primeira barreira impeditiva é a receita bruta anual, que deve ser inferior a R$ 3.600.000,00 (três milhões e seiscentos mil reais). À partir de 2018 esse limite será de R$ 4.800.000,00 (quatro milhões e oitocentos mil reais).</p>
             <br>
             <p>Os Microempreendedores Individuais (MEI) terão também um novo teto que passará de R$ 60 mil para R$ 81 mil ao ano, passando de uma média de R$ 5 mil ao mês para R$ 6,75 mil.</p>
             <br>
             <p>Quanto aos Autônomos é feito o Livro Caixa e a emissão de Darf Mensal relativo ao Carnê ou Mensalão.</p>
